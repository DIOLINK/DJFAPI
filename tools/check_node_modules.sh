#!/bin/bash
# Valida node_modules para frontend (spa-client)
CYAN='\033[0;36m'; NC='\033[0m'
FRONTEND_DIR="spa-client"
if [ ! -d "$FRONTEND_DIR/node_modules" ]; then
  echo -e "${CYAN}No está instalado node_modules en $FRONTEND_DIR.${NC}"
  read -rp "¿Correr pnpm install? (y/n): " instpm
  if [ "$instpm" = "y" ]; then
    (cd "$FRONTEND_DIR" && pnpm install)
  fi
fi
